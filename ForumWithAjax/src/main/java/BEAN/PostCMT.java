package BEAN;

public class PostCMT {
	private String name;
	private String usercontent;
	
	public PostCMT() {
		
	}
	

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUsercontent() {
		return usercontent;
	}
	public void setUsercontent(String usercontent) {
		this.usercontent = usercontent;
	}
	
	
	
	
}
